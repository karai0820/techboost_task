４、cat コマンドの役割と使い方を調べてみましょう。また、catコマンドを使って3.で作ったファイルの中身を見てみましょう。

$ cat:ファイルの中身を閲覧、結合する
$ cat ファイル名
→ファイルの中身を出力する。

$ cat ファイルA >　ファイルB
→ファイルAの中身を新規ファイルBを作成し、書き込む。ファイルBが既に存在した場合は上書きする。

$ cat ファイルA >> ファイルB
→ファイルAの中身をファイルBに追加する

$ cat ファイルA ファイルB ファイルC >ファイルD
→ファイルDにファイルA、ファイルB、ファイルCの内容を結合して書き込む（上書きする）

オプション
-n:行番号を付けて出力する
-b:空白行を入れずに行番号を付けて表示する。

